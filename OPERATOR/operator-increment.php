<?php
echo "<h3>Latihan Increment</h3>";

$x = 15;

echo "Nilai awal: $x <br>";

//Post-increment 
echo "Post-increment (x++): " . $x++ . "<br>";
echo "Setelah post-increment, x = $x <br><br>";

//Pre-increment
echo "pre-increment (++x): " . ++$x . "<br>";
echo "Setelah pre-increment, x = $x <br>";
?>